
public class Booking {
	Flight flight;
	Seat seat;
	public Flight getFlight() {
		return flight;
	}
	public void setFlight(Flight flight) {
		this.flight = flight;
	}
	public Seat getSeat() {
		return seat;
	}
	public void setSeat(Seat seat) {
		this.seat = seat;
	}
	public Booking(Flight flight, Seat string) {
		super();
		this.flight = flight;
		this.seat = string;
	}
}
